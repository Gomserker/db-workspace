package com.bg.unit;

public class Danwi {
	private int num;
	private String unit;
	private String danwi2;
	private String cn;
	private String result2;
	
	public Danwi(int num, String unit, String danwi2, String cn, String result2) {
		super();
		this.num = num;
		this.unit = unit;
		this.danwi2 = danwi2;
		this.cn = cn;
		this.result2 = result2;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getDanwi2() {
		return danwi2;
	}

	public void setDanwi2(String danwi2) {
		this.danwi2 = danwi2;
	}

	public String getCn() {
		return cn;
	}

	public void setCn(String cn) {
		this.cn = cn;
	}

	public String getResult2() {
		return result2;
	}

	public void setResult2(String result2) {
		this.result2 = result2;
	}
	
	
}
